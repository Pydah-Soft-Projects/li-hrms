'use client';

import React, { useState, useEffect, useMemo } from 'react';
import { Calendar, Download, Search, Filter, Plus, Settings, Eye } from 'lucide-react';
import { api } from '@/lib/api';
import { toast } from 'react-hot-toast';

interface Employee {
    employeeId: string;
    empNo: string;
    employeeName: string;
    designation: string;
    department: string;
    divisionName: string;
    clBalance: number;
    elBalance: number;
    compensatoryOffBalance: number;
    presentMonthAllowedLeaves: number;
    cumulativeLeaves: number;
}

interface LeaveTransaction {
    _id: string;
    leaveType: string;
    transactionType: string;
    startDate: string;
    endDate: string;
    days: number;
    openingBalance: number;
    closingBalance: number;
    status: string;
    reason: string;
    applicationDate: string;
    approvalDate: string;
    autoGenerated: boolean;
    payrollCycleStart: string;
    payrollCycleEnd: string;
    financialYear: string;
}

interface EmployeeDetail {
    employeeId: string;
    empNo: string;
    employeeName: string;
    designation: string;
    department: string;
    divisionName: string;
    dateOfJoining: string;
    employmentStatus: string;
    leaveTypes: {
        [key: string]: {
            openingBalance: number;
            closingBalance: number;
            credits: number;
            debits: number;
            transactions: LeaveTransaction[];
        };
    };
}

export default function LeaveRegisterPage() {
    const [employees, setEmployees] = useState<Employee[]>([]);
    const [loading, setLoading] = useState(true);
    const [searchTerm, setSearchTerm] = useState('');
    const [selectedEmployee, setSelectedEmployee] = useState<EmployeeDetail | null>(null);
    const [showDetailModal, setShowDetailModal] = useState(false);
    const [showAdjustModal, setShowAdjustModal] = useState(false);
    const [adjustmentData, setAdjustmentData] = useState({
        employeeId: '',
        employeeName: '',
        empNo: '',
        clBalance: 0,
        newBalance: '', // target CL balance – creates ADJUSTMENT transaction
        reason: ''
    });
    const [showEmployeeDetailModal, setShowEmployeeDetailModal] = useState(false);
    const [selectedEmployeeDetail, setSelectedEmployeeDetail] = useState(null);
    const [selectedMonth, setSelectedMonth] = useState(new Date().getMonth() + 1);
    const [selectedYear, setSelectedYear] = useState(new Date().getFullYear());

    useEffect(() => {
        fetchLeaveRegister();
    }, [selectedMonth, selectedYear]);

    const fetchLeaveRegister = async () => {
        try {
            setLoading(true);
            const data = await api.getLeaveRegister({
                month: selectedMonth,
                year: selectedYear
            });
            
            if (data.success) {
                // Backend returns groupByEmployeeMonthly: { employee, casualLeave, earnedLeave, compensatoryOff, totalPaidBalance, monthlyAllowedLimit }
                const formattedEmployees = (data.data || []).map((emp: any) => {
                    const employee = emp.employee || {};
                    const employeeId = employee.id || emp.employeeId?._id || emp.employeeId?.id || emp.employeeId;
                    const casualLeave = emp.casualLeave || {};
                    const earnedLeave = emp.earnedLeave || {};
                    const backendMonthlyLimit = typeof emp.monthlyAllowedLimit === 'number'
                        ? emp.monthlyAllowedLimit
                        : (casualLeave.balance || 0) + (earnedLeave.balance || 0) + ((emp.compensatoryOff && emp.compensatoryOff.balance) || 0);
                    return {
                        employeeId,
                        empNo: employee.empNo ?? emp.empNo,
                        employeeName: employee.name ?? emp.employeeName,
                        designation: employee.designation ?? emp.designation,
                        department: employee.department ?? emp.department,
                        divisionName: employee.division ?? emp.divisionName ?? 'N/A',
                        clBalance: casualLeave.balance ?? 0,
                        elBalance: earnedLeave.balance ?? 0,
                        compensatoryOffBalance: (emp.compensatoryOff?.balance) ?? 0,
                        // Use backend-calculated monthly allowed limit (CL + CCL + optional EL)
                        presentMonthAllowedLeaves: backendMonthlyLimit,
                        cumulativeLeaves: emp.totalPaidBalance ?? 0
                    };
                });
                setEmployees(formattedEmployees);
            } else {
                toast.error('Failed to fetch leave register');
            }
        } catch (error) {
            console.error('Error fetching leave register:', error);
            toast.error('Error fetching leave register');
        } finally {
            setLoading(false);
        }
    };

    const fetchEmployeeDetail = async (employeeId: string) => {
        try {
            const data = await api.getEmployeeRegister(employeeId);
            if (data.success) {
                setSelectedEmployeeDetail(data.data);
                setShowEmployeeDetailModal(true);
            } else {
                toast.error('Failed to fetch employee details');
            }
        } catch (error) {
            console.error('Error fetching employee details:', error);
            toast.error('Error fetching employee details');
        }
    };

    const openAdjustModal = (employee: Employee) => {
        setAdjustmentData({
            employeeId: employee.employeeId,
            employeeName: employee.employeeName,
            empNo: employee.empNo,
            clBalance: employee.clBalance,
            newBalance: String(employee.clBalance),
            reason: ''
        });
        setShowAdjustModal(true);
    };

    const handleAdjustment = async () => {
        const newBal = parseFloat(adjustmentData.newBalance);
        if (adjustmentData.newBalance === '' || isNaN(newBal) || newBal < 0) {
            toast.error('Please enter a valid new balance (0 or more)');
            return;
        }
        if (!adjustmentData.reason.trim()) {
            toast.error('Please enter a reason for the adjustment');
            return;
        }

        try {
            const result = await api.adjustLeaveBalance({
                employeeId: adjustmentData.employeeId,
                leaveType: 'CL',
                amount: newBal,
                transactionType: 'ADJUSTMENT',
                reason: adjustmentData.reason.trim()
            });

            if (result.success) {
                toast.success('CL balance adjusted successfully');
                setShowAdjustModal(false);
                fetchLeaveRegister(); // Refresh data
            } else {
                toast.error(result.message || 'Failed to adjust CL balance');
            }
        } catch (error) {
            console.error('Error adjusting CL balance:', error);
            toast.error('Error adjusting CL balance');
        }
    };

    const exportToExcel = () => {
        // TODO: Implement Excel export
        toast.success('Export feature coming soon!');
    };

    const filteredEmployees = useMemo(() => {
        if (!searchTerm.trim()) return employees;
        const term = searchTerm.toLowerCase().trim();
        return employees.filter(
            (emp) =>
                (emp.employeeName || '').toLowerCase().includes(term) ||
                (emp.empNo || '').toLowerCase().includes(term) ||
                (emp.designation || '').toLowerCase().includes(term) ||
                (emp.department || '').toLowerCase().includes(term) ||
                (emp.divisionName || '').toLowerCase().includes(term)
        );
    }, [employees, searchTerm]);

    if (loading) {
        return (
            <div className="flex items-center justify-center min-h-96">
                <div className="animate-spin rounded-full h-12 w-12 border-2 border-gray-200 dark:border-gray-700 border-t-indigo-600 dark:border-t-indigo-500"></div>
            </div>
        );
    }

    return (
        <div className="space-y-6">
            {/* Header */}
            <div className="flex flex-wrap items-center justify-between gap-4">
                <div>
                    <h1 className="text-2xl font-bold text-gray-900 dark:text-white">Leave Register</h1>
                    <p className="text-gray-500 dark:text-gray-400 mt-0.5">Complete leave ledger for all employees</p>
                </div>
                <button
                    onClick={exportToExcel}
                    className="flex items-center gap-2 px-4 py-2.5 bg-indigo-600 text-white rounded-lg hover:bg-indigo-700 transition-colors text-sm font-medium"
                >
                    <Download className="w-4 h-4" />
                    Export
                </button>
            </div>

            {/* Filters */}
            <section className="bg-white dark:bg-[#1E293B] rounded-xl border border-gray-200 dark:border-gray-800 p-5">
                <div className="flex flex-wrap items-center gap-3 mb-1">
                    <Filter className="w-4 h-4 text-gray-500 dark:text-gray-400" />
                    <span className="text-sm font-medium text-gray-700 dark:text-gray-300">Filters</span>
                </div>
                <div className="grid grid-cols-1 md:grid-cols-3 gap-4 mt-2">
                    <div className="relative">
                        <Search className="absolute left-3 top-1/2 -translate-y-1/2 text-gray-400 dark:text-gray-500 w-4 h-4" />
                        <input
                            type="text"
                            placeholder="Search by name, emp no, designation, department..."
                            value={searchTerm}
                            onChange={(e) => setSearchTerm(e.target.value)}
                            className="pl-10 pr-4 py-2.5 w-full rounded-xl border border-gray-200 dark:border-gray-700 bg-gray-50/50 dark:bg-gray-800/30 text-gray-900 dark:text-gray-100 placeholder-gray-500 dark:placeholder-gray-400 focus:ring-2 focus:ring-indigo-500 focus:border-transparent"
                        />
                    </div>
                    <div className="flex items-center gap-2">
                        <Calendar className="w-4 h-4 text-gray-500 dark:text-gray-400 shrink-0" />
                        <select
                            value={selectedMonth}
                            onChange={(e) => setSelectedMonth(parseInt(e.target.value))}
                            className="flex-1 min-w-0 px-3 py-2.5 rounded-xl border border-gray-200 dark:border-gray-700 bg-gray-50/50 dark:bg-gray-800/30 text-gray-900 dark:text-gray-100 focus:ring-2 focus:ring-indigo-500 focus:border-transparent"
                        >
                            {[...Array(12)].map((_, i) => (
                                <option key={i} value={i + 1}>
                                    {new Date(0, i).toLocaleString('default', { month: 'long' })}
                                </option>
                            ))}
                        </select>
                    </div>
                    <div className="flex items-center gap-2">
                        <Calendar className="w-4 h-4 text-gray-500 dark:text-gray-400 shrink-0" />
                        <select
                            value={selectedYear}
                            onChange={(e) => setSelectedYear(parseInt(e.target.value))}
                            className="flex-1 min-w-0 px-3 py-2.5 rounded-xl border border-gray-200 dark:border-gray-700 bg-gray-50/50 dark:bg-gray-800/30 text-gray-900 dark:text-gray-100 focus:ring-2 focus:ring-indigo-500 focus:border-transparent"
                        >
                            {[2024, 2025, 2026, 2027].map(year => (
                                <option key={year} value={year}>{year}</option>
                            ))}
                        </select>
                    </div>
                </div>
            </section>

            {/* Employee Table */}
            <section className="bg-white dark:bg-[#1E293B] rounded-xl border border-gray-200 dark:border-gray-800 overflow-hidden">
                <div className="overflow-x-auto">
                    <table className="w-full">
                        <thead className="bg-gray-50 dark:bg-gray-800/50">
                            <tr>
                                <th className="px-4 py-3 text-left text-xs font-medium text-gray-500 dark:text-gray-400 uppercase tracking-wider">
                                    Employee
                                </th>
                                <th className="px-4 py-3 text-left text-xs font-medium text-gray-500 dark:text-gray-400 uppercase tracking-wider">
                                    Designation
                                </th>
                                <th className="px-4 py-3 text-left text-xs font-medium text-gray-500 dark:text-gray-400 uppercase tracking-wider">
                                    Department
                                </th>
                                <th className="px-4 py-3 text-left text-xs font-medium text-gray-500 dark:text-gray-400 uppercase tracking-wider">
                                    Division
                                </th>
                                <th className="px-4 py-3 text-center text-xs font-medium text-gray-500 dark:text-gray-400 uppercase tracking-wider">
                                    CL
                                </th>
                                <th className="px-4 py-3 text-center text-xs font-medium text-gray-500 dark:text-gray-400 uppercase tracking-wider">
                                    EL
                                </th>
                                <th className="px-4 py-3 text-center text-xs font-medium text-gray-500 dark:text-gray-400 uppercase tracking-wider">
                                    Comp Off
                                </th>
                                <th className="px-4 py-3 text-center text-xs font-medium text-gray-500 dark:text-gray-400 uppercase tracking-wider">
                                    Month limit
                                </th>
                                <th className="px-4 py-3 text-center text-xs font-medium text-gray-500 dark:text-gray-400 uppercase tracking-wider">
                                    Cumulative
                                </th>
                                <th className="px-4 py-3 text-center text-xs font-medium text-gray-500 dark:text-gray-400 uppercase tracking-wider">
                                    Actions
                                </th>
                            </tr>
                        </thead>
                        <tbody className="divide-y divide-gray-200 dark:divide-gray-700">
                            {filteredEmployees.map((employee) => (
                                <tr key={employee.employeeId} className="hover:bg-gray-50 dark:hover:bg-gray-800/50 transition-colors">
                                    <td className="px-4 py-3">
                                        <div>
                                            <div className="font-medium text-gray-900 dark:text-white">{employee.employeeName || '—'}</div>
                                            <div className="text-xs text-gray-500 dark:text-gray-400">{employee.empNo || '—'}</div>
                                        </div>
                                    </td>
                                    <td className="px-4 py-3 text-sm text-gray-700 dark:text-gray-300">{employee.designation || '—'}</td>
                                    <td className="px-4 py-3 text-sm text-gray-700 dark:text-gray-300">{employee.department || '—'}</td>
                                    <td className="px-4 py-3 text-sm text-gray-700 dark:text-gray-300">{employee.divisionName || '—'}</td>
                                    <td className="px-4 py-3 text-center">
                                        <span className={`inline-flex px-2 py-1 text-xs font-semibold rounded-full ${
                                            employee.clBalance > 0 ? 'bg-green-100 dark:bg-green-900/30 text-green-800 dark:text-green-300' : 'bg-red-100 dark:bg-red-900/30 text-red-800 dark:text-red-300'
                                        }`}>
                                            {employee.clBalance}
                                        </span>
                                    </td>
                                    <td className="px-4 py-3 text-center">
                                        <span className={`inline-flex px-2 py-1 text-xs font-semibold rounded-full ${
                                            employee.elBalance > 0 ? 'bg-green-100 dark:bg-green-900/30 text-green-800 dark:text-green-300' : 'bg-red-100 dark:bg-red-900/30 text-red-800 dark:text-red-300'
                                        }`}>
                                            {employee.elBalance}
                                        </span>
                                    </td>
                                    <td className="px-4 py-3 text-center">
                                        <span className={`inline-flex px-2 py-1 text-xs font-semibold rounded-full ${
                                            employee.compensatoryOffBalance > 0 ? 'bg-green-100 dark:bg-green-900/30 text-green-800 dark:text-green-300' : 'bg-gray-100 dark:bg-gray-700/50 text-gray-700 dark:text-gray-400'
                                        }`}>
                                            {employee.compensatoryOffBalance}
                                        </span>
                                    </td>
                                    <td className="px-4 py-3 text-center text-sm text-gray-900 dark:text-gray-100 font-medium">
                                        {employee.presentMonthAllowedLeaves}
                                    </td>
                                    <td className="px-4 py-3 text-center text-sm text-gray-900 dark:text-gray-100">
                                        {employee.cumulativeLeaves}
                                    </td>
                                    <td className="px-4 py-3">
                                        <div className="flex items-center justify-center gap-2">
                                            <button
                                                onClick={() => fetchEmployeeDetail(employee.employeeId)}
                                                className="inline-flex items-center gap-1 px-2.5 py-1.5 text-sm text-indigo-600 dark:text-indigo-400 hover:bg-indigo-50 dark:hover:bg-indigo-900/20 rounded-lg transition-colors"
                                            >
                                                <Eye className="w-4 h-4" />
                                                View
                                            </button>
                                            <button
                                                onClick={() => openAdjustModal(employee)}
                                                className="inline-flex items-center gap-1 px-2.5 py-1.5 text-sm text-emerald-600 dark:text-emerald-400 hover:bg-emerald-50 dark:hover:bg-emerald-900/20 rounded-lg transition-colors"
                                            >
                                                <Settings className="w-4 h-4" />
                                                Adjust
                                            </button>
                                        </div>
                                    </td>
                                </tr>
                            ))}
                        </tbody>
                    </table>
                </div>
                {filteredEmployees.length === 0 && (
                    <div className="text-center py-12 text-gray-500 dark:text-gray-400">
                        No employees to show for the selected period. Try another month or clear the search.
                    </div>
                )}
            </section>

            {/* Employee Detail Modal */}
            {showDetailModal && selectedEmployee && (
                <div className="fixed inset-0 bg-black bg-opacity-50 flex items-center justify-center z-50">
                    <div className="bg-white rounded-lg w-full max-w-6xl max-h-[90vh] overflow-hidden">
                        <div className="p-6 border-b">
                            <div className="flex items-center justify-between">
                                <h2 className="text-xl font-bold">Employee Leave Register</h2>
                                <button
                                    onClick={() => setShowDetailModal(false)}
                                    className="text-gray-400 hover:text-gray-600"
                                >
                                    ×
                                </button>
                            </div>
                        </div>
                        
                        <div className="p-6 overflow-y-auto max-h-[calc(90vh-200px)]">
                            {/* Employee Basic Info */}
                            <div className="bg-gray-50 rounded-lg p-4 mb-6">
                                <h3 className="font-semibold mb-3">Employee Information</h3>
                                <div className="grid grid-cols-2 md:grid-cols-4 gap-4">
                                    <div>
                                        <label className="text-sm text-gray-500">Name</label>
                                        <p className="font-medium">{selectedEmployee.employeeName}</p>
                                    </div>
                                    <div>
                                        <label className="text-sm text-gray-500">Employee No</label>
                                        <p className="font-medium">{selectedEmployee.empNo}</p>
                                    </div>
                                    <div>
                                        <label className="text-sm text-gray-500">Designation</label>
                                        <p className="font-medium">{selectedEmployee.designation}</p>
                                    </div>
                                    <div>
                                        <label className="text-sm text-gray-500">Department</label>
                                        <p className="font-medium">{selectedEmployee.department}</p>
                                    </div>
                                    <div>
                                        <label className="text-sm text-gray-500">Date of Joining</label>
                                        <p className="font-medium">{new Date(selectedEmployee.dateOfJoining).toLocaleDateString()}</p>
                                    </div>
                                    <div>
                                        <label className="text-sm text-gray-500">Employment Status</label>
                                        <p className="font-medium">{selectedEmployee.employmentStatus}</p>
                                    </div>
                                </div>
                            </div>

                            {/* Current Balances */}
                            <div className="grid grid-cols-1 md:grid-cols-3 gap-4 mb-6">
                                {Object.entries(selectedEmployee.leaveTypes).map(([leaveType, data]) => (
                                    <div key={leaveType} className="bg-white border rounded-lg p-4">
                                        <h4 className="font-semibold mb-2">{leaveType} Balance</h4>
                                        <div className="space-y-1">
                                            <div className="flex justify-between">
                                                <span className="text-sm text-gray-500">Opening:</span>
                                                <span className="font-medium">{data.openingBalance}</span>
                                            </div>
                                            <div className="flex justify-between">
                                                <span className="text-sm text-gray-500">Credits:</span>
                                                <span className="font-medium text-green-600">+{data.credits}</span>
                                            </div>
                                            <div className="flex justify-between">
                                                <span className="text-sm text-gray-500">Debits:</span>
                                                <span className="font-medium text-red-600">-{data.debits}</span>
                                            </div>
                                            <div className="flex justify-between border-t pt-1">
                                                <span className="text-sm font-medium">Closing:</span>
                                                <span className="font-bold">{data.closingBalance}</span>
                                            </div>
                                        </div>
                                    </div>
                                ))}
                            </div>

                            {/* Transactions */}
                            <div>
                                <h3 className="font-semibold mb-3">All Transactions</h3>
                                <div className="overflow-x-auto">
                                    <table className="w-full text-sm">
                                        <thead className="bg-gray-50">
                                            <tr>
                                                <th className="px-3 py-2 text-left">Date</th>
                                                <th className="px-3 py-2 text-left">Type</th>
                                                <th className="px-3 py-2 text-left">Transaction</th>
                                                <th className="px-3 py-2 text-center">Days</th>
                                                <th className="px-3 py-2 text-center">Opening</th>
                                                <th className="px-3 py-2 text-center">Closing</th>
                                                <th className="px-3 py-2 text-left">Status</th>
                                                <th className="px-3 py-2 text-left">Reason</th>
                                                <th className="px-3 py-2 text-left">Auto</th>
                                            </tr>
                                        </thead>
                                        <tbody>
                                            {Object.values(selectedEmployee.leaveTypes).flatMap((leaveType) =>
                                                leaveType.transactions.map((transaction, index) => (
                                                    <tr key={`${transaction._id}-${index}`} className="border-b">
                                                        <td className="px-3 py-2">
                                                            {new Date(transaction.startDate).toLocaleDateString()}
                                                        </td>
                                                        <td className="px-3 py-2">
                                                            <span className={`px-2 py-1 text-xs rounded ${
                                                                transaction.leaveType === 'EL' ? 'bg-blue-100 text-blue-800' :
                                                                transaction.leaveType === 'CL' ? 'bg-green-100 text-green-800' :
                                                                transaction.leaveType === 'SL' ? 'bg-yellow-100 text-yellow-800' :
                                                                'bg-gray-100 text-gray-800'
                                                            }`}>
                                                                {transaction.leaveType}
                                                            </span>
                                                        </td>
                                                        <td className="px-3 py-2">
                                                            <span className={`px-2 py-1 text-xs rounded ${
                                                                transaction.transactionType === 'CREDIT' ? 'bg-green-100 text-green-800' :
                                                                transaction.transactionType === 'DEBIT' ? 'bg-red-100 text-red-800' :
                                                                'bg-gray-100 text-gray-800'
                                                            }`}>
                                                                {transaction.transactionType}
                                                            </span>
                                                        </td>
                                                        <td className="px-3 py-2 text-center">{transaction.days}</td>
                                                        <td className="px-3 py-2 text-center">{transaction.openingBalance}</td>
                                                        <td className="px-3 py-2 text-center font-medium">{transaction.closingBalance}</td>
                                                        <td className="px-3 py-2">
                                                            <span className={`px-2 py-1 text-xs rounded ${
                                                                transaction.status === 'APPROVED' ? 'bg-green-100 text-green-800' :
                                                                transaction.status === 'PENDING' ? 'bg-yellow-100 text-yellow-800' :
                                                                'bg-gray-100 text-gray-800'
                                                            }`}>
                                                                {transaction.status}
                                                            </span>
                                                        </td>
                                                        <td className="px-3 py-2 text-sm">{transaction.reason}</td>
                                                        <td className="px-3 py-2 text-center">
                                                            {transaction.autoGenerated && (
                                                                <span className="text-blue-600">✓</span>
                                                            )}
                                                        </td>
                                                    </tr>
                                                ))
                                            )}
                                        </tbody>
                                    </table>
                                </div>
                            </div>
                        </div>
                    </div>
                </div>
            )}

            {/* CL Balance Adjustment Modal */}
            {showAdjustModal && (
                <div className="fixed inset-0 bg-black/60 dark:bg-black/70 flex items-center justify-center z-50 p-4">
                    <div className="bg-white dark:bg-[#1E293B] rounded-xl border border-gray-200 dark:border-gray-800 w-full max-w-md shadow-xl">
                        <div className="flex items-center justify-between p-5 border-b border-gray-200 dark:border-gray-700">
                            <h2 className="text-lg font-semibold text-gray-900 dark:text-white">Adjust CL Balance</h2>
                            <button
                                onClick={() => setShowAdjustModal(false)}
                                className="p-1.5 text-gray-500 hover:text-gray-700 dark:hover:text-gray-300 rounded-lg hover:bg-gray-100 dark:hover:bg-gray-800"
                            >
                                ×
                            </button>
                        </div>
                        <div className="p-5 space-y-4">
                            <div>
                                <label className="block text-sm font-medium text-gray-700 dark:text-gray-300 mb-1.5">Employee</label>
                                <input
                                    type="text"
                                    value={adjustmentData.employeeName ? `${adjustmentData.employeeName} (${adjustmentData.empNo})` : adjustmentData.employeeId}
                                    disabled
                                    className="w-full px-3 py-2.5 rounded-xl border border-gray-200 dark:border-gray-700 bg-gray-50 dark:bg-gray-800/50 text-gray-700 dark:text-gray-300"
                                />
                            </div>
                            <div>
                                <label className="block text-sm font-medium text-gray-700 dark:text-gray-300 mb-1.5">Current CL Balance</label>
                                <input
                                    type="text"
                                    value={adjustmentData.clBalance}
                                    readOnly
                                    className="w-full px-3 py-2.5 rounded-xl border border-gray-200 dark:border-gray-700 bg-gray-50 dark:bg-gray-800/50 text-gray-700 dark:text-gray-300"
                                />
                            </div>
                            <div>
                                <label className="block text-sm font-medium text-gray-700 dark:text-gray-300 mb-1.5">New balance</label>
                                <input
                                    type="number"
                                    min={0}
                                    step={0.5}
                                    value={adjustmentData.newBalance}
                                    onChange={(e) => setAdjustmentData({ ...adjustmentData, newBalance: e.target.value })}
                                    className="w-full px-3 py-2.5 rounded-xl border border-gray-200 dark:border-gray-700 bg-white dark:bg-gray-800/30 text-gray-900 dark:text-gray-100 focus:ring-2 focus:ring-indigo-500 focus:border-transparent"
                                    placeholder="e.g. 12"
                                />
                                <p className="text-xs text-gray-500 dark:text-gray-400 mt-1">An ADJUSTMENT transaction will be created.</p>
                            </div>
                            <div>
                                <label className="block text-sm font-medium text-gray-700 dark:text-gray-300 mb-1.5">Reason (required)</label>
                                <textarea
                                    value={adjustmentData.reason}
                                    onChange={(e) => setAdjustmentData({ ...adjustmentData, reason: e.target.value })}
                                    className="w-full px-3 py-2.5 rounded-xl border border-gray-200 dark:border-gray-700 bg-white dark:bg-gray-800/30 text-gray-900 dark:text-gray-100 focus:ring-2 focus:ring-indigo-500 focus:border-transparent"
                                    rows={3}
                                    placeholder="Enter reason for adjustment"
                                />
                            </div>
                            <div className="flex gap-3 pt-2">
                                <button
                                    onClick={() => setShowAdjustModal(false)}
                                    className="flex-1 px-4 py-2.5 rounded-xl border border-gray-300 dark:border-gray-600 text-gray-700 dark:text-gray-300 hover:bg-gray-50 dark:hover:bg-gray-800 font-medium"
                                >
                                    Cancel
                                </button>
                                <button
                                    onClick={handleAdjustment}
                                    className="flex-1 px-4 py-2.5 bg-indigo-600 text-white rounded-xl hover:bg-indigo-700 font-medium transition-colors"
                                >
                                    Adjust Balance
                                </button>
                            </div>
                        </div>
                    </div>
                </div>
            )}

            {/* Employee Detail Modal (View ledger) */}
            {showEmployeeDetailModal && selectedEmployeeDetail && (
                <div className="fixed inset-0 bg-black/60 dark:bg-black/70 flex items-center justify-center z-50 p-4">
                    <div className="bg-white dark:bg-[#1E293B] rounded-xl border border-gray-200 dark:border-gray-800 w-full max-w-4xl max-h-[90vh] overflow-hidden shadow-xl flex flex-col">
                        <div className="flex justify-between items-center p-5 border-b border-gray-200 dark:border-gray-700 shrink-0">
                            <h3 className="text-lg font-semibold text-gray-900 dark:text-white">
                                Leave ledger — {selectedEmployeeDetail.employeeName}
                            </h3>
                            <button
                                onClick={() => setShowEmployeeDetailModal(false)}
                                className="p-1.5 text-gray-500 hover:text-gray-700 dark:hover:text-gray-300 rounded-lg hover:bg-gray-100 dark:hover:bg-gray-800"
                            >
                                ✕
                            </button>
                        </div>
                        <div className="p-5 overflow-y-auto space-y-6">
                            <div className="rounded-xl border border-gray-200 dark:border-gray-700 p-4 bg-gray-50/50 dark:bg-gray-800/30">
                                <h4 className="font-medium text-gray-900 dark:text-white mb-3">Employee</h4>
                                <div className="grid grid-cols-2 sm:grid-cols-4 gap-3 text-sm">
                                    <div><span className="text-gray-500 dark:text-gray-400">Emp No</span><br /><span className="text-gray-900 dark:text-white">{selectedEmployeeDetail.empNo}</span></div>
                                    <div><span className="text-gray-500 dark:text-gray-400">Name</span><br /><span className="text-gray-900 dark:text-white">{selectedEmployeeDetail.employeeName}</span></div>
                                    <div><span className="text-gray-500 dark:text-gray-400">Designation</span><br /><span className="text-gray-900 dark:text-white">{selectedEmployeeDetail.designation ?? '—'}</span></div>
                                    <div><span className="text-gray-500 dark:text-gray-400">Department</span><br /><span className="text-gray-900 dark:text-white">{selectedEmployeeDetail.department ?? '—'}</span></div>
                                    <div><span className="text-gray-500 dark:text-gray-400">DOJ</span><br /><span className="text-gray-900 dark:text-white">{selectedEmployeeDetail.dateOfJoining ? new Date(selectedEmployeeDetail.dateOfJoining).toLocaleDateString() : '—'}</span></div>
                                </div>
                            </div>
                            <div>
                                <h4 className="font-medium text-gray-900 dark:text-white mb-3">Leave balances</h4>
                                <div className="grid grid-cols-1 md:grid-cols-3 gap-4">
                                    {Object.entries(selectedEmployeeDetail.leaveTypes).map(([leaveType, details]: [string, any]) => (
                                        <div key={leaveType} className="rounded-xl border border-gray-200 dark:border-gray-700 p-4 bg-white dark:bg-gray-800/30">
                                            <h5 className="font-medium text-gray-900 dark:text-white mb-2">{leaveType}</h5>
                                            <div className="space-y-1 text-sm text-gray-700 dark:text-gray-300">
                                                <div className="flex justify-between"><span>Opening</span><span>{details.openingBalance}</span></div>
                                                <div className="flex justify-between"><span>Credits</span><span className="text-green-600 dark:text-green-400">+{details.credits}</span></div>
                                                <div className="flex justify-between"><span>Debits</span><span className="text-red-600 dark:text-red-400">-{details.debits}</span></div>
                                                <div className="flex justify-between font-medium pt-1 border-t border-gray-200 dark:border-gray-700">Closing<span>{details.closingBalance}</span></div>
                                            </div>
                                        </div>
                                    ))}
                                </div>
                            </div>
                            <div>
                                <h4 className="font-medium text-gray-900 dark:text-white mb-3">Transaction history</h4>
                                {Object.entries(selectedEmployeeDetail.leaveTypes).map(([leaveType, details]: [string, any]) => (
                                    <div key={leaveType} className="mb-6">
                                        <h5 className="text-sm font-medium text-gray-700 dark:text-gray-300 mb-2">{leaveType}</h5>
                                        <div className="overflow-x-auto rounded-xl border border-gray-200 dark:border-gray-700">
                                            <table className="min-w-full text-sm">
                                                <thead className="bg-gray-50 dark:bg-gray-800/50">
                                                    <tr>
                                                        <th className="px-4 py-2 text-left text-xs font-medium text-gray-500 dark:text-gray-400">Date</th>
                                                        <th className="px-4 py-2 text-left text-xs font-medium text-gray-500 dark:text-gray-400">Type</th>
                                                        <th className="px-4 py-2 text-left text-xs font-medium text-gray-500 dark:text-gray-400">Days</th>
                                                        <th className="px-4 py-2 text-left text-xs font-medium text-gray-500 dark:text-gray-400">Opening</th>
                                                        <th className="px-4 py-2 text-left text-xs font-medium text-gray-500 dark:text-gray-400">Closing</th>
                                                        <th className="px-4 py-2 text-left text-xs font-medium text-gray-500 dark:text-gray-400">Reason</th>
                                                    </tr>
                                                </thead>
                                                <tbody className="divide-y divide-gray-200 dark:divide-gray-700">
                                                    {details.transactions.map((transaction: any, index: number) => (
                                                        <tr key={index} className="bg-white dark:bg-transparent">
                                                            <td className="px-4 py-2 text-gray-900 dark:text-gray-100">{new Date(transaction.startDate).toLocaleDateString()}</td>
                                                            <td className="px-4 py-2">
                                                                <span className={`px-2 py-0.5 text-xs rounded-full ${
                                                                    transaction.transactionType === 'CREDIT' ? 'bg-green-100 dark:bg-green-900/30 text-green-800 dark:text-green-300' : 'bg-red-100 dark:bg-red-900/30 text-red-800 dark:text-red-300'
                                                                }`}>
                                                                    {transaction.transactionType}
                                                                </span>
                                                            </td>
                                                            <td className="px-4 py-2 text-gray-900 dark:text-gray-100">{transaction.days}</td>
                                                            <td className="px-4 py-2 text-gray-700 dark:text-gray-300">{transaction.openingBalance}</td>
                                                            <td className="px-4 py-2 text-gray-700 dark:text-gray-300">{transaction.closingBalance}</td>
                                                            <td className="px-4 py-2 text-gray-600 dark:text-gray-400">{transaction.reason ?? '—'}</td>
                                                        </tr>
                                                    ))}
                                                </tbody>
                                            </table>
                                        </div>
                                    </div>
                                ))}
                            </div>
                        </div>
                    </div>
                </div>
            )}
        </div>
    );
}
