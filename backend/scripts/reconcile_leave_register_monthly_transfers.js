/**
 * Reconcile LeaveRegisterYear monthly pool transfer rows for CL/CCL/EL.
 *
 * What it does:
 *   1) Strips MONTHLY_POOL_TRANSFER_IN_* and MONTHLY_POOL_TRANSFER_OUT_* rows
 *      for all payroll edges from the FY opening through the target month opening.
 *   2) Reverts slot pool bumps recorded in poolCarryForwardIn.
 *   3) Recomputes actual unused pool from current slot credits minus actual used
 *      debits (net of old reversal credits) and locked CL.
 *   4) Reposts matching transfer-out/transfer-in rows and refreshes balances.
 *
 * Usage:
 *   node scripts/reconcile_leave_register_monthly_transfers.js --empNo 61 --untilMonth 5 --untilYear 2026
 *   node scripts/reconcile_leave_register_monthly_transfers.js --empNo 61 --untilMonth 5 --untilYear 2026 --apply
 *
 * Defaults:
 *   --financialYear <name> defaults to the FY containing today's date
 *   dry-run unless --apply is provided
 */

