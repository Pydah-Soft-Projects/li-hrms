const mongoose = require('mongoose');
require('dotenv').config();

async function run() {
  await mongoose.connect(process.env.MONGODB_URI);
  const db = mongoose.connection.db;
  const doc = await db.collection('leave_register_years').findOne({ empNo: '984', financialYear: '2026' });
  if(doc) {
    const slot = doc.months.find(m => m.payrollCycleMonth === 4 && m.payrollCycleYear === 2026);
    if(slot) {
      console.log('Slot 4/2026 for 984:');
      console.log('monthlyApplyApproved:', slot.monthlyApplyApproved);
      console.log('Transactions:');
      slot.transactions.filter(t => (t.transactionType || "").toUpperCase() === 'DEBIT').forEach(t => {
        console.log(`  ${t.leaveType} ${t.days} status:${t.status} agType:${t.autoGeneratedType} reason:${t.reason}`);
      });
    }
  }
  process.exit(0);
}

run().catch(console.error);
