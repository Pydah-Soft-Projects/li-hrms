const mongoose = require('mongoose');
require('dotenv').config();

async function run() {
  await mongoose.connect(process.env.MONGODB_URI);
  const db = mongoose.connection.db;

  const docs = await db.collection('leave_register_years').find({ empNo: { $exists: true } }).limit(500).toArray();
  console.log(`Checking ${docs.length} documents...`);

  let discrepancyCount = 0;
  for (const doc of docs) {
    for (let i = 0; i < doc.months.length; i++) {
      const slot = doc.months[i];
      const txs = slot.transactions || [];
      
      const ledgerUsed = txs
        .filter(t => String(t.transactionType || '').trim().toUpperCase() === 'DEBIT')
        .filter(t => {
          const ag = String(t.autoGeneratedType || '');
          if (ag.startsWith('MONTHLY_POOL_TRANSFER_OUT_')) return false;
          return true;
        })
        .reduce((s, t) => s + (Number(t.days) || 0), 0);

      const approved = Number(slot.monthlyApplyApproved) || 0;
      
      if (Math.abs(ledgerUsed - approved) > 0.01) {
        console.log(`Discrepancy found! Emp: ${doc.empNo}, Month: ${slot.payrollCycleMonth}/${slot.payrollCycleYear}`);
        console.log(`  ledgerUsed: ${ledgerUsed}, monthlyApplyApproved: ${approved}`);
        discrepancyCount++;
      }
    }
  }

  console.log(`Total discrepancies found: ${discrepancyCount}`);
  process.exit(0);
}

run().catch(console.error);
