const LeaveRegisterYear = require('../model/LeaveRegisterYear');
const LeavePolicySettings = require('../../settings/model/LeavePolicySettings');
const dateCycleService = require('./dateCycleService');
const leaveRegisterYearLedgerService = require('./leaveRegisterYearLedgerService');
const leaveRegisterYearMonthlyApplyService = require('./leaveRegisterYearMonthlyApplyService');

const LEAVE_TYPES = ['CL', 'CCL', 'EL'];
const TRANSFER_OUT_PREFIX = 'MONTHLY_POOL_TRANSFER_OUT_';
const TRANSFER_IN_PREFIX = 'MONTHLY_POOL_TRANSFER_IN_';

function round2(value) {
  const n = Number(value) || 0;
  return Math.round(n * 100) / 100;
}

function roundHalf(value) {
  const n = Number(value) || 0;
  if (n <= 0) return 0;
  return Math.round(n * 2) / 2;
}

function sortSlotRefs(months) {
  return (months || [])
    .map((slot, idx) => ({
      idx,
      slot,
      time: new Date(slot?.payPeriodStart || slot?.payPeriodEnd || 0).getTime(),
    }))
    .sort((a, b) => a.time - b.time);
}

function transferAutoType(direction, leaveType) {
  return `${direction === 'out' ? TRANSFER_OUT_PREFIX : TRANSFER_IN_PREFIX}${leaveType}`;
}

function isTransferTx(tx) {
  const autoType = String(tx?.autoGeneratedType || '');
  return autoType.startsWith(TRANSFER_OUT_PREFIX) || autoType.startsWith(TRANSFER_IN_PREFIX);
}

function isTransferOutTx(tx) {
  return String(tx?.autoGeneratedType || '').startsWith(TRANSFER_OUT_PREFIX);
}

function isReversalCredit(tx) {
  return (
    String(tx?.transactionType || '').toUpperCase() === 'CREDIT' &&
    String(tx?.reason || '').includes('Leave Application Cancelled/Reversed')
  );
}

function sumMonthlyClSchedule(slot) {
  let total = 0;
  for (const tx of slot?.transactions || []) {
    if (String(tx?.leaveType || '').toUpperCase() !== 'CL') continue;
    if (String(tx?.transactionType || '').toUpperCase() !== 'CREDIT') continue;
    if (String(tx?.autoGeneratedType || '') !== 'MONTHLY_CL_SCHEDULE') continue;
    total += Math.max(0, Number(tx?.days) || 0);
  }
  return round2(total);
}

function reconcileSlotScheduledCl(slot) {
  const scheduledTx = sumMonthlyClSchedule(slot);
  if (scheduledTx <= 0) return false;
  const transferIn = roundHalf(Number(slot?.poolCarryForwardIn?.cl) || 0);
  const expected = roundHalf(scheduledTx + transferIn);
  const before = roundHalf(Number(slot.clCredits) || 0);
  if (before === expected) return false;
  slot.clCredits = expected;
  return true;
}

function netUsedDaysForType(slot, leaveType) {
  const lt = String(leaveType || '').toUpperCase();
  let debits = 0;
  let reversalCredits = 0;
  for (const tx of slot?.transactions || []) {
    if (String(tx?.leaveType || '').toUpperCase() !== lt) continue;
    if (isTransferTx(tx)) continue;
    const txType = String(tx?.transactionType || '').toUpperCase();
    const days = Math.max(0, Number(tx?.days) || 0);
    if (txType === 'DEBIT') debits += days;
    if (isReversalCredit(tx)) reversalCredits += days;
  }
  return roundHalf(Math.max(0, debits - reversalCredits));
}

function resolveRollFlags(policy) {
  const capCfg = policy?.monthlyLeaveApplicationCap || {};
  const includeEL = !!capCfg.includeEL;
  const elPaidInPayroll = policy?.earnedLeave?.useAsPaidInPayroll !== false;
  const elInPool = !!policy?.earnedLeave?.enabled && includeEL && !elPaidInPayroll;
  return {
    CL: policy?.carryForward?.casualLeave?.carryMonthlyClCreditToNextPayrollMonth !== false,
    CCL: policy?.carryForward?.compensatoryOff?.carryMonthlyPoolToNextPayrollMonth !== false,
    EL: elInPool && policy?.carryForward?.earnedLeave?.carryMonthlyPoolToNextPayrollMonth !== false,
    elInPool,
  };
}

function computeCarry(slot, rollFlags) {
  const credits = {
    CL: roundHalf(Number(slot?.clCredits) || 0),
    CCL: roundHalf(Number(slot?.compensatoryOffs) || 0),
    EL: rollFlags.elInPool ? roundHalf(Number(slot?.elCredits) || 0) : 0,
  };
  const used = {
    CL: netUsedDaysForType(slot, 'CL'),
    CCL: netUsedDaysForType(slot, 'CCL'),
    EL: netUsedDaysForType(slot, 'EL'),
  };
  const monthLocked = roundHalf(
    Math.max(Number(slot?.lockedCredits) || 0, Number(slot?.monthlyApplyLocked) || 0)
  );
  const locked = { CL: 0, CCL: 0, EL: 0 };
  let remainingLocked = monthLocked;
  for (const lt of LEAVE_TYPES) {
    if (lt === 'EL' && !rollFlags.elInPool) continue;
    const availableAfterUsed = roundHalf(Math.max(0, credits[lt] - used[lt]));
    locked[lt] = roundHalf(Math.min(remainingLocked, availableAfterUsed));
    remainingLocked = roundHalf(Math.max(0, remainingLocked - locked[lt]));
  }
  return {
    credits,
    used: { ...used, monthLocked },
    locked,
    unallocatedLocked: remainingLocked,
    carry: {
      CL: rollFlags.CL ? roundHalf(Math.max(0, credits.CL - used.CL - locked.CL)) : 0,
      CCL: rollFlags.CCL ? roundHalf(Math.max(0, credits.CCL - used.CCL - locked.CCL)) : 0,
      EL: rollFlags.EL ? roundHalf(Math.max(0, credits.EL - used.EL - locked.EL)) : 0,
    },
  };
}

function monthKey(slot) {
  return `${Number(slot?.payrollCycleMonth)}/${Number(slot?.payrollCycleYear)}`;
}

function findSlotIndexByPayroll(ordered, month, year) {
  return ordered.findIndex(
    ({ slot }) =>
      Number(slot?.payrollCycleMonth) === Number(month) &&
      Number(slot?.payrollCycleYear) === Number(year)
  );
}

function findSlotIndexForDate(ordered, date) {
  const t = new Date(date).getTime();
  if (!Number.isFinite(t)) return -1;
  return ordered.findIndex(({ slot }) => {
    const start = new Date(slot?.payPeriodStart || 0).getTime();
    const end = new Date(slot?.payPeriodEnd || 0).getTime();
    return Number.isFinite(start) && Number.isFinite(end) && start <= t && t <= end;
  });
}

function findTargetIndex(ordered, targetPeriod, asOf) {
  let targetIdx = findSlotIndexByPayroll(ordered, targetPeriod?.month, targetPeriod?.year);
  if (targetIdx >= 0) return targetIdx;

  const asOfTime = new Date(asOf || new Date()).getTime();
  for (let p = ordered.length - 1; p >= 0; p--) {
    const start = new Date(ordered[p]?.slot?.payPeriodStart || 0).getTime();
    if (Number.isFinite(start) && start <= asOfTime) return p;
  }
  return ordered.length - 1;
}

function stripTransfersFromSourceThroughTarget(doc, sourceIdx, targetIdx) {
  const ordered = sortSlotRefs(doc.months);
  const stats = {
    removedTransferRows: 0,
    revertedCarryInSlots: 0,
    scheduledClFixedSlots: 0,
  };

  for (let p = sourceIdx; p <= targetIdx; p++) {
    const slot = ordered[p]?.slot;
    if (!slot) continue;

    if (p > sourceIdx) {
      const pin = slot.poolCarryForwardIn || {};
      const dCl = roundHalf(Number(pin.cl) || 0);
      const dCcl = roundHalf(Number(pin.ccl) || 0);
      const dEl = roundHalf(Number(pin.el) || 0);
      if (dCl || dCcl || dEl) {
        slot.clCredits = roundHalf(Math.max(0, (Number(slot.clCredits) || 0) - dCl));
        slot.compensatoryOffs = roundHalf(Math.max(0, (Number(slot.compensatoryOffs) || 0) - dCcl));
        slot.elCredits = roundHalf(Math.max(0, (Number(slot.elCredits) || 0) - dEl));
        stats.revertedCarryInSlots++;
      }
      slot.poolCarryForwardIn = { cl: 0, ccl: 0, el: 0 };
      slot.poolCarryForwardFromLabel = '';
    }

    const before = Array.isArray(slot.transactions) ? slot.transactions.length : 0;
    if (p === sourceIdx) {
      slot.transactions = (slot.transactions || []).filter((tx) => !isTransferOutTx(tx));
    } else {
      slot.transactions = (slot.transactions || []).filter((tx) => !isTransferTx(tx));
    }
    stats.removedTransferRows += Math.max(0, before - slot.transactions.length);

    slot.poolCarryForwardOut = { cl: 0, ccl: 0, el: 0 };
    slot.poolCarryForwardOutAt = null;
    slot.poolCarryForwardAllocation = undefined;
    if (reconcileSlotScheduledCl(slot)) stats.scheduledClFixedSlots++;
  }

  return stats;
}

function pushTransferTx(slot, leaveType, txType, days, autoGeneratedType, date, reason) {
  const d = roundHalf(days);
  if (d <= 0) return;
  if (!Array.isArray(slot.transactions)) slot.transactions = [];
  slot.transactions.push({
    at: new Date(),
    leaveType,
    transactionType: txType,
    days: d,
    openingBalance: 0,
    closingBalance: 0,
    startDate: date,
    endDate: date,
    reason,
    status: 'APPROVED',
    autoGenerated: true,
    autoGeneratedType,
  });
}

function rebuildTransfersFromSourceThroughTarget(doc, sourceIdx, targetIdx, rollFlags) {
  const ordered = sortSlotRefs(doc.months);
  const stats = { rebuiltEdges: 0, lockedClClearedSlots: 0 };

  for (let p = sourceIdx; p < targetIdx; p++) {
    const cur = ordered[p]?.slot;
    const next = ordered[p + 1]?.slot;
    if (!cur || !next) continue;

    const c = computeCarry(cur, rollFlags);
    const carry = c.carry;
    const fromLabel = monthKey(cur);
    const toLabel = monthKey(next);
    const lockedCreditsReleasedToTransferOut = roundHalf(Number(cur.lockedCredits) || 0);

    if (lockedCreditsReleasedToTransferOut > 0) stats.lockedClClearedSlots++;
    cur.lockedCredits = 0;
    cur.poolCarryForwardAllocation = {
      source: 'monthlyTransferReconciliationService',
      credits: c.credits,
      actualUsed: c.used,
      lockedDeducted: c.locked,
      unallocatedLocked: c.unallocatedLocked,
      lockedCreditsReleasedToTransferOut,
      carryPosted: {
        cl: carry.CL,
        ccl: carry.CCL,
        el: carry.EL,
      },
      rollFlags,
    };

    if (carry.CL <= 0 && carry.CCL <= 0 && carry.EL <= 0) continue;

    cur.poolCarryForwardOut = {
      cl: carry.CL,
      ccl: carry.CCL,
      el: carry.EL,
    };
    cur.poolCarryForwardOutAt = new Date();

    next.clCredits = roundHalf((Number(next.clCredits) || 0) + carry.CL);
    next.compensatoryOffs = roundHalf((Number(next.compensatoryOffs) || 0) + carry.CCL);
    next.elCredits = roundHalf((Number(next.elCredits) || 0) + carry.EL);
    next.poolCarryForwardIn = {
      cl: carry.CL,
      ccl: carry.CCL,
      el: carry.EL,
    };
    next.poolCarryForwardFromLabel = cur.label || fromLabel;
    reconcileSlotScheduledCl(next);

    const outAt = cur.payPeriodEnd ? new Date(cur.payPeriodEnd) : new Date(cur.payPeriodStart || new Date());
    const inAt = next.payPeriodStart ? new Date(next.payPeriodStart) : new Date(next.payPeriodEnd || outAt);

    for (const lt of LEAVE_TYPES) {
      if (carry[lt] <= 0) continue;
      pushTransferTx(
        cur,
        lt,
        'DEBIT',
        carry[lt],
        transferAutoType('out', lt),
        outAt,
        `Reconciled transfer out unused monthly pool ${lt} from payroll ${fromLabel} to ${toLabel}.`
      );
      pushTransferTx(
        next,
        lt,
        'CREDIT',
        carry[lt],
        transferAutoType('in', lt),
        inAt,
        `Reconciled transfer in unused monthly pool ${lt} from payroll ${fromLabel} to ${toLabel}.`
      );
    }
    stats.rebuiltEdges++;
  }

  return stats;
}

async function syncTouchedSlots(employeeId, doc, sourceIdx, targetIdx) {
  const ordered = sortSlotRefs(doc.months);
  const seen = new Set();
  for (let p = sourceIdx; p <= targetIdx; p++) {
    const slot = ordered[p]?.slot;
    const anchor = slot?.payPeriodStart || slot?.payPeriodEnd;
    if (!anchor) continue;
    const key = new Date(anchor).toISOString();
    if (seen.has(key)) continue;
    seen.add(key);
    await leaveRegisterYearMonthlyApplyService.syncStoredMonthApplyFieldsForEmployeeDate(employeeId, anchor);
  }
}

async function reconcileEmployeeFromDate({
  employeeId,
  fromDate,
  untilDate = new Date(),
  untilMonth,
  untilYear,
  financialYear,
  apply = true,
} = {}) {
  if (!employeeId || !fromDate) return { ok: false, skipped: true, reason: 'missing_employee_or_date' };

  const sourcePeriod = await dateCycleService.getPeriodInfo(fromDate);
  const targetPeriod =
    untilMonth && untilYear
      ? { payrollCycle: { month: Number(untilMonth), year: Number(untilYear) } }
      : await dateCycleService.getPeriodInfo(untilDate);
  const fyName = financialYear || sourcePeriod.financialYear?.name;
  const doc = await LeaveRegisterYear.findOne({ employeeId, financialYear: fyName });
  if (!doc?.months?.length) {
    return { ok: true, skipped: true, reason: 'register_year_not_found', financialYear: fyName };
  }

  const ordered = sortSlotRefs(doc.months);
  let sourceIdx = findSlotIndexByPayroll(
    ordered,
    sourcePeriod.payrollCycle?.month,
    sourcePeriod.payrollCycle?.year
  );
  if (sourceIdx < 0) sourceIdx = findSlotIndexForDate(ordered, fromDate);
  if (sourceIdx < 0) {
    return { ok: true, skipped: true, reason: 'source_slot_not_found', financialYear: fyName };
  }

  let targetIdx = findTargetIndex(ordered, targetPeriod.payrollCycle, untilDate);
  if (targetIdx < sourceIdx) targetIdx = sourceIdx;

  const policy = await LeavePolicySettings.getSettings().catch(() => ({}));
  const rollFlags = resolveRollFlags(policy);
  const stripStats = stripTransfersFromSourceThroughTarget(doc, sourceIdx, targetIdx);
  const rebuildStats = rebuildTransfersFromSourceThroughTarget(doc, sourceIdx, targetIdx, rollFlags);

  if (apply) {
    doc.markModified('months');
    await doc.save();
    for (const lt of LEAVE_TYPES) {
      await leaveRegisterYearLedgerService.recalculateRegisterBalances(employeeId, lt, fromDate);
    }
    await syncTouchedSlots(employeeId, doc, sourceIdx, targetIdx);
  }

  return {
    ok: true,
    skipped: false,
    employeeId: String(employeeId),
    financialYear: fyName,
    source: monthKey(ordered[sourceIdx]?.slot),
    target: monthKey(ordered[targetIdx]?.slot),
    apply,
    ...stripStats,
    ...rebuildStats,
  };
}

async function reconcileForLeaveChange(leaveRecord, options = {}) {
  if (!leaveRecord?.employeeId || !leaveRecord?.fromDate) {
    return { ok: false, skipped: true, reason: 'invalid_leave_record' };
  }
  return reconcileEmployeeFromDate({
    employeeId: leaveRecord.employeeId,
    fromDate: leaveRecord.fromDate,
    ...options,
  });
}

