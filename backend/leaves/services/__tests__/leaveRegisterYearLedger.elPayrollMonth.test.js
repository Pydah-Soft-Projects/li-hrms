/**
 * Ensures month-level idempotency for EL PAYROLL_USE debits (one debit per employee per payroll YYYY-MM).
 */

jest.mock('../../model/LeaveRegisterYear');

const LeaveRegisterYear = require('../../model/LeaveRegisterYear');
const { findExistingPayrollElDebitForPayrollMonth } = require('../leaveRegisterYearLedgerService');

describe('findExistingPayrollElDebitForPayrollMonth', () => {
  beforeEach(() => {
    jest.clearAllMocks();
  });

  test('returns null when no matching transaction', async () => {
    LeaveRegisterYear.find = jest.fn().mockReturnValue({
      lean: jest.fn().mockResolvedValue([
        {
          months: [
            {
              payrollCycleYear: 2026,
              payrollCycleMonth: 4,
              transactions: [{ leaveType: 'EL', transactionType: 'CREDIT', autoGeneratedType: 'EARNED_LEAVE' }],
            },
          ],
        },
      ]),
    });

    const r = await findExistingPayrollElDebitForPayrollMonth('507f1f77bcf86cd799439011', '2026-04');
    expect(r).toBeNull();
  });

  test('returns first EL DEBIT PAYROLL_USE in the payroll cycle month', async () => {
    const hit = { leaveType: 'EL', transactionType: 'DEBIT', autoGeneratedType: 'PAYROLL_USE', days: 2 };
    LeaveRegisterYear.find = jest.fn().mockReturnValue({
      lean: jest.fn().mockResolvedValue([
        {
          months: [
            {
              payrollCycleYear: 2026,
              payrollCycleMonth: 3,
              transactions: [],
            },
            {
              payrollCycleYear: 2026,
              payrollCycleMonth: 4,
              transactions: [hit],
            },
          ],
        },
      ]),
    });

    const r = await findExistingPayrollElDebitForPayrollMonth('507f1f77bcf86cd799439011', '2026-04');
    expect(r).toEqual(hit);
  });

  test('returns null for invalid month string', async () => {
    LeaveRegisterYear.find = jest.fn();
    const r = await findExistingPayrollElDebitForPayrollMonth('507f1f77bcf86cd799439011', 'bad');
    expect(r).toBeNull();
    expect(LeaveRegisterYear.find).not.toHaveBeenCalled();
  });
});
