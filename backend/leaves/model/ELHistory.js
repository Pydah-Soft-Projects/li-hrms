const mongoose = require('mongoose');

/**
 * Earned Leave History
 * Stores a compact audit trail of EL credits/debits by month/year.
 */
const elHistorySchema = new mongoose.Schema({
  employeeId: { type: mongoose.Schema.Types.ObjectId, ref: 'Employee', required: true, index: true },
  empNo: { type: String, required: true, index: true },
  month: { type: Number, required: true }, // 1-12 payroll month
  year: { type: Number, required: true },
  days: { type: Number, required: true },
  type: { type: String, enum: ['CREDIT', 'DEBIT'], default: 'CREDIT' },
  source: {
    type: String,
    enum: ['AUTO_ACCRUAL', 'PAYROLL_USE', 'MANUAL_ADJUSTMENT'],
    default: 'AUTO_ACCRUAL',
  },
  autoGeneratedType: { type: String, default: null },
  reason: { type: String, default: '' },
  breakdown: { type: mongoose.Schema.Types.Mixed },
}, { timestamps: true });

elHistorySchema.index({ employeeId: 1, year: 1, month: 1, source: 1 });

module.exports = mongoose.models.ELHistory || mongoose.model('ELHistory', elHistorySchema);

