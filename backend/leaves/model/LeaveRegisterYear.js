const mongoose = require('mongoose');

/**
 * Per-employee leave register for one financial year.
 * months[] = policy schedule (clCredits/elCredits/…) + movements in months[].transactions (single ledger).
 * yearlyTransactions = optional FY-level audit summary (carry-forward, reset narrative).
 */

const monthMovementSchema = new mongoose.Schema(
  {
    at: { type: Date, default: Date.now },
    leaveType: { type: String, required: true },
    transactionType: {
      type: String,
      enum: ['CREDIT', 'DEBIT', 'ADJUSTMENT', 'CARRY_FORWARD', 'EXPIRY'],
      required: true,
    },
    days: { type: Number, default: 0 },
    openingBalance: { type: Number, default: 0 },
    closingBalance: { type: Number, default: 0 },
    startDate: { type: Date, required: true },
    endDate: { type: Date, required: true },
    reason: { type: String, default: '' },
    applicationId: { type: mongoose.Schema.Types.ObjectId, ref: 'Leave' },
    applicationDate: { type: Date },
    approvalDate: { type: Date },
    approvedBy: { type: mongoose.Schema.Types.ObjectId, ref: 'Employee' },
    status: { type: String, default: 'APPROVED' },
    autoGenerated: { type: Boolean, default: false },
    autoGeneratedType: { type: String, default: null },
    includedInPayroll: { type: Boolean, default: false },
    payrollBatchId: { type: mongoose.Schema.Types.ObjectId, ref: 'PayrollBatch' },
    calculationBreakdown: { type: mongoose.Schema.Types.Mixed },
    monthlyCLLimit: { type: Number },
    monthlyCCLLimit: { type: Number },
    monthlyELLimit: { type: Number },
    monthlyCombinedLimit: { type: Number },
    pendingLeavesBeforeAction: { type: Number },
    divisionId: { type: mongoose.Schema.Types.ObjectId, ref: 'Division' },
    departmentId: { type: mongoose.Schema.Types.ObjectId, ref: 'Department' },
  },
  { _id: true }
);

const monthSlotSchema = new mongoose.Schema(
  {
    payrollMonthIndex: { type: Number, required: true, min: 1, max: 12 },
    label: { type: String, default: '' },
    payPeriodStart: { type: Date, required: true },
    payPeriodEnd: { type: Date, required: true },
    payrollCycleMonth: { type: Number, min: 1, max: 12 },
    payrollCycleYear: { type: Number },
    clCredits: { type: Number, default: 0 },
    elCredits: { type: Number, default: 0 },
    compensatoryOffs: { type: Number, default: 0 },
    /** Optional policy/admin lock; in-flight application holds are computed from Leave rows (see register view). */
    lockedCredits: { type: Number, default: 0 },
    /** min(scheduled CL+CCL[+EL per policy], monthly cap); refreshed on sync / slot build. */
    monthlyApplyCeiling: { type: Number },
    /** Days counting toward apply cap (pending + approved), denormalized from Leave. */
    monthlyApplyConsumed: { type: Number },
    monthlyApplyLocked: { type: Number },
    monthlyApplyApproved: { type: Number },
    monthlyApplySyncedAt: { type: Date },
    /**
     * Payroll cycle close: cap consumption (monthlyApplyConsumed) allocated CL → CCL → EL;
     * remainder may carry to next slot or forfeit (policy).
     */
    poolCarryForwardAllocation: { type: mongoose.Schema.Types.Mixed },
    poolCarryForwardOut: {
      cl: { type: Number, default: 0 },
      ccl: { type: Number, default: 0 },
      el: { type: Number, default: 0 },
    },
    poolCarryForwardOutAt: { type: Date },
    /** Opening: amounts rolled into this slot from previous payroll month (audit; also added to clCredits/compensatoryOffs/elCredits). */
    poolCarryForwardIn: {
      cl: { type: Number, default: 0 },
      ccl: { type: Number, default: 0 },
      el: { type: Number, default: 0 },
    },
    poolCarryForwardFromLabel: { type: String, default: '' },
    /** All leave movements attributed to this payroll period (CL / EL / CCL / …). */
    transactions: { type: [monthMovementSchema], default: [] },
  },
  { _id: false }
);

const yearTransactionSchema = new mongoose.Schema(
  {
    at: { type: Date, default: Date.now },
    transactionKind: {
      type: String,
      enum: ['CREDIT', 'DEBIT', 'ADJUSTMENT', 'CARRY_FORWARD', 'EXPIRY'],
      required: true
    },
    leaveType: {
      type: String,
      enum: ['CL', 'EL', 'CCL'],
      default: 'CL'
    },
    days: { type: Number, default: 0 },
    reason: { type: String, default: '' },
    payrollMonthIndex: { type: Number, min: 1, max: 12 },
    payPeriodStart: { type: Date },
    payPeriodEnd: { type: Date },
    meta: { type: mongoose.Schema.Types.Mixed }
  },
  { _id: true }
);

const leaveRegisterYearSchema = new mongoose.Schema(
  {
    employeeId: {
      type: mongoose.Schema.Types.ObjectId,
      ref: 'Employee',
      required: true,
      index: true
    },
    empNo: { type: String, required: true, index: true },
    employeeName: { type: String, default: '' },
    financialYear: { type: String, required: true, index: true },
    financialYearStart: { type: Date, required: true },
    financialYearEnd: { type: Date, required: true },
    /** Snapshot CL balance after annual reset adjustment (opening for the year from ledger perspective). */
    casualBalance: { type: Number, default: 0 },
    /**
     * Sum of months[].clCredits after last reset/sync (12 payroll slots: tier monthly grid + join rules;
     * carry-forward is folded into period 1). Matches sum of MONTHLY_CL_SCHEDULE credits for the year row.
     */
    yearlyPolicyClScheduledTotal: { type: Number, default: 0 },
    /**
     * Running CCL balance from LeaveRegisterYear transactions (updated on each CCL recalc).
     * Also set from Employee snapshot when the FY row is first created.
     */
    compensatoryOffBalance: { type: Number, default: 0 },
    /**
     * Running EL balance from LeaveRegisterYear transactions (updated on each EL recalc).
     * Also set from Employee snapshot when the FY row is first created.
     */
    earnedLeaveBalance: { type: Number, default: 0 },
    /** FY audit: sum of days on CL CREDIT ledger rows (scheduled/monthly/manual). */
    yearlyClCreditDaysPosted: { type: Number, default: 0 },
    /** FY audit: sum of days on CCL CREDIT rows (e.g. holiday OD). */
    yearlyCclCreditDaysPosted: { type: Number, default: 0 },
    months: { type: [monthSlotSchema], default: [] },
    yearlyTransactions: { type: [yearTransactionSchema], default: [] },
    resetAt: { type: Date },
    /** ANNUAL_RESET = year rollover; MANUAL = admin; EMPLOYEE_ONBOARDING = new hire; INITIAL_POLICY_SYNC = HR initial sync from policy */
    source: {
      type: String,
      enum: ['ANNUAL_RESET', 'MANUAL', 'EMPLOYEE_ONBOARDING', 'INITIAL_POLICY_SYNC'],
      default: 'ANNUAL_RESET'
    }
  },
  {
    timestamps: true,
    collection: 'leave_register_years'
  }
);

leaveRegisterYearSchema.index({ employeeId: 1, financialYear: 1 }, { unique: true });

module.exports = mongoose.model('LeaveRegisterYear', leaveRegisterYearSchema);
